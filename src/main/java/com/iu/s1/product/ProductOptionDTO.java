package com.iu.s1.product;

public class ProductOptionDTO {
	private Integer option_num;
	private Integer product_num;
	private String option_name;
	private Integer option_price;
	private Integer option_each;
	public Integer getOption_num() {
		return option_num;
	}
	public void setOption_num(Integer option_num) {
		this.option_num = option_num;
	}
	public Integer getProduct_num() {
		return product_num;
	}
	public void setProduct_num(Integer product_num) {
		this.product_num = product_num;
	}
	public String getOption_name() {
		return option_name;
	}
	public void setOption_name(String option_name) {
		this.option_name = option_name;
	}
	public Integer getOption_price() {
		return option_price;
	}
	public void setOption_price(Integer option_price) {
		this.option_price = option_price;
	}
	public Integer getOption_each() {
		return option_each;
	}
	public void setOption_each(Integer option_each) {
		this.option_each = option_each;
	}
	
}
